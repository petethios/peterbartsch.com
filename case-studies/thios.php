            <section class="case-study-section">
                <h2>The Strategic Hypothesis</h2>
                <p><strong>How might a senior product leader leverage AI tools to operate at the velocity of a full team?</strong> I wanted to test whether an experienced generalist could compress traditional product development stages—strategy, research, design, engineering—into a solo workflow with AI as a scalable collaborator.</p>
                <p><strong>The goal wasn't just to build a product. It was to validate a model that any startup could use to run leaner.</strong></p>
            </section>

            <section class="case-study-section case-study-constraints">
                <h2>Why This Was Hard</h2>
                <p>This wasn't a software side project—it was a physical product with real constraints:</p>
                <ul>
                    <li><strong>Solo execution:</strong> No team, no funding—every role from strategy to CAD to code to marketing</li>
                    <li><strong>Physical + digital:</strong> Not just software—actual shelter systems requiring manufacturing knowledge</li>
                    <li><strong>Time compression:</strong> 18 months to prove the model from zero to revenue</li>
                    <li><strong>Tool evolution:</strong> AI capabilities rapidly shifting—had to continuously adapt workflows</li>
                    <li><strong>Quality bar:</strong> Had to be indistinguishable from team-built work to validate the model</li>
                </ul>
                <p style="margin-top: 16px; margin-bottom: 0;"><strong>If this only worked because standards were lowered, it wouldn't prove anything useful.</strong></p>
            </section>

            <section class="case-study-section">
                <h2>Strategy</h2>
                <p>I treated AI as a <strong>technical co-founder</strong>, not a tool. This meant:</p>
                <ul>
                    <li><strong>Role-Based Collaboration:</strong> Different AI tools for different phases—research, design, development, CAD</li>
                    <li><strong>Conversational Development:</strong> Iterating through dialogue, not just prompts—building context over time</li>
                    <li><strong>Quality-First:</strong> Never ship something I wouldn't ship with a team—AI accelerates, doesn't excuse</li>
                    <li><strong>Continuous Adaptation:</strong> Workflow evolved as tools improved—Copilot → Cursor → Claude Code</li>
                </ul>
            </section>

            <section class="case-study-section">
                <h2>Execution</h2>
                <h3 style="font-family: var(--font-display); font-size: 16px; color: var(--color-white); margin: 24px 0 12px 0;">Research & Strategy</h3>
                <p>Obsidian MD + AI for project management and research synthesis. Built a knowledge base that AI could reference across sessions.</p>

                <h3 style="font-family: var(--font-display); font-size: 16px; color: var(--color-white); margin: 24px 0 12px 0;">Brand & Visual Design</h3>
                <p>Figma for visual design with AI-assisted design-to-code workflows. Brand identity inspired by early 2000s open-source culture.</p>

                <h3 style="font-family: var(--font-display); font-size: 16px; color: var(--color-white); margin: 24px 0 12px 0;">Web Development</h3>
                <p>Progressed through GitHub Copilot → Cursor → Claude Code as tools matured. Each evolution compressed cycles further. Built 3 production websites.</p>

                <h3 style="font-family: var(--font-display); font-size: 16px; color: var(--color-white); margin: 24px 0 12px 0;">Physical Product</h3>
                <p>OnShape for CAD modeling with AI helping translate concepts into precise specifications. 5 shelter variants designed and one physical prototype built.</p>
            </section>

            <section class="case-study-section">
                <h2>Results</h2>
                <ul>
                    <li>Delivered a <strong>complete product ecosystem</strong> in 18 months: brand, 3 websites, 5 shelter variants, CAD models, physical prototype, revenue-generating handbook</li>
                    <li>Compressed prototyping cycles <strong>70% faster</strong> through conversational development</li>
                    <li>Built a <strong>3D configurator</strong> (Three.js + React) that lets customers design custom shelters</li>
                    <li>Validated <strong>transferable patterns</strong> for AI-augmented solo work applicable to product organizations</li>
                </ul>
            </section>

            <section class="case-study-section">
                <h2>What This Proves</h2>
                <p>This work demonstrates:</p>
                <ul>
                    <li>That <strong>AI-augmented workflows are ready for production use</strong>—not just prototypes</li>
                    <li>That <strong>experienced generalists can operate across boundaries</strong> that traditionally required specialists</li>
                    <li>That <strong>the patterns are transferable</strong>—any startup or product team can run leaner with the right approach</li>
                </ul>
                <p><strong>The real outcome was proving that the future of product development isn't larger teams—it's better leverage.</strong></p>
            </section>

