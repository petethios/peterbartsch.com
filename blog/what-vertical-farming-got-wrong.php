                <h2>The Pattern</h2>
                <p>The VC-funded vertical farming industry followed a predictable 5-step pattern of failure: massive capital raise, centralized factory build-out, unit economics that never work at scale, pivot attempts, and collapse. Companies like AeroFarms, AppHarvest, and Infarm all followed the same playbook.</p>

                <h2>Why This Matters for Shelter</h2>
                <p>The same pattern has played out in pre-fab housing. Katerra raised $2B to centralize home construction and went bankrupt. The lesson: inherently local industries — agriculture, construction, shelter — cannot be solved with centralized, capital-intensive approaches.</p>

                <h2>What the Survivors Did Differently</h2>
                <p>The companies that survived shared three traits: they worked with existing local supply chains instead of replacing them, they built platforms instead of products, and they kept capital requirements low enough to reach profitability before running out of runway.</p>

                <h2>Platform vs. Product</h2>
                <p>Product companies try to own the entire value chain. Platform companies provide the tools, designs, and coordination layer while local operators handle execution. The platform approach matches the distributed nature of local industries — you don't need a factory in every city when you have a blueprint system that works with any fabricator.</p>

                <h2>The Lesson</h2>
                <p>Don't centralize what's inherently distributed. Don't replace local expertise with capital-intensive automation. Build the operating system, not the factory. This is the design philosophy behind Thios — open-source shelter designs that work with local materials and local builders.</p>
