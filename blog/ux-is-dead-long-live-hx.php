                <p><strong>tl;dr</strong> UX is dead, because we get to design for humans now.</p>

                <p>User Experience (UX) has long suffered from its own misnomer: "User." Which we often mistook for "Human." Humans are not equal to Users in agency. We far exceed the abilities of any User. That's why we use the term. Our tech is novel and immature compared to ourselves. The hardware and software limited and primitive, if evolving fast. We design for the User because we could not design for actual Humans.</p>

                <p>What started with actual buttons and switches and levers, evolved to the command line, and its higher fidelity input. Then on to "Point and Click" GUIs. And somewhere, as the controls got more complex, Humans became Users in an attempt to acknowledge the limits of interaction. Even with today's sensor laden devices that can ID our face, hear our murmurs, track our hands, and global position, the Machines vs Users metaphor persists. The Interaction stilted.</p>

                <h2>Fewest variables for the widest net</h2>

                <p>The Machines were not able to create bespoke interactions for every User's context. So the largest potential user base meant the fewest variables in the experience. We could not produce something in all languages, so we designed towards universal icons.</p>

                <p>We've used the term User because our tools couldn't interact like Humans. And because it helpfully managed expectations. But this also created a meta-persona of Users as limited in agency, random in nature, easily distracted, and increasingly so lately, emotionally manipulated. Add to all this a shout out to Cory Doctorow for his insights on platform enshitification, and I was not feeling very positive about things.</p>

                <p>To be honest, digital design for me was starting to feel like it was on an unalterably path towards Cory's definition. Experiences becoming commodified and confirmed by data for the largest possible user base. Experiences with the least variables. Every AirBnB the same, damn the GPS.</p>

                <h2>Google Zero my hero?</h2>

                <p>Since Gen AI started getting its legs, I've been struggling to see what the future of UX was. I can chat with Figma now, and vibe code a prototype. From the standpoint of communicating design ideas, those tools are amazing. And we are witnessing a fundamental change in the work, tool and team-flows for product development and management. Exciting times for sure. But what of the User?</p>

                <p>With all the recent chatter about "Google Zero" and that event horizon leading to the interesting concept of primarily designing for the Agent's Experience (AX). I was kinda stopped in my tracks. Is this the end of UX I wondered?</p>

                <h2>UX is dead. Long live HX!</h2>

                <p>Firstly we can now almost drop the translation layer. It gets thin and native. The Agent is a native speaking bilingual; Machine and Human. Via Agents, the Machines can now create bespoke experiences to the Humans context and abilities.</p>

                <p>Designing for Agents at first feels like it would quickly become an order taking exercise as the two sides find native efficiencies, tuned to their abilities and context. Maybe within the Agent's future experiences there's a world of novel and amazing Machine to Machine interactions to be created. And maybe increasingly those AX moments will be unfathomable to Humans.</p>

                <p>But secondly, and more interestingly I think, when you design for AX, you are not designing for a "User." You are designing for an operator that is exclusively logical, unemotional, solely task focused with infinite patience and hyper speed. The dark patterns that work so well on us random emotional humans on the web won't find traction with Agents.</p>

                <p>Therefore AX brings a level of honesty and transparency to the experience that we have not been designing towards for a very long time.</p>

                <p>And I don't think you can put it back in the bottle either. Once the AX is not just faster and/or easier, once it is the more honest and transparent experience — who's going to buy your click bait anymore?</p>

                <h2>AX requires HX</h2>

                <p>This is why I believe, along with designing for the Machine to Machine side of things, we will be forced to design for Human Experience (HX) too. Once your Agent returns from task for your feedback — it's Agent to Human communications. An Agent that works for you and speaks your language. We can design the Agent to know the human and their context. To remember past interactions and preferences. We will demand and they will be able to deliver a more transparent and honest experience. Because we can create a more Human interaction now.</p>

                <blockquote>"Agent! Read me all my spam aloud and lie to me." — Nobody</blockquote>

                <p>As a kid in the early 80's that once got a week with an Apple II, and spent it not coding, but making a pixel image with basic commands, (hplot 0,30, vplot 30, 30...) Gen AI has been making computers feel that level of fun again for me for a while now.</p>

                <p>But now that I realize designing for Agents means designing for Humans, not Users. It's like a whole new world of interesting design challenges appearing before us. Now this is an exciting time! This positive realization takes things to a whole new level for me. And all us Humans really. I just hope the level boss isn't from Ibiza.</p>
